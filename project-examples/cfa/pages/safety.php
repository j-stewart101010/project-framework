								<h3 class="title">Preparing your property</h3>
								<p>There are some simple things you can do around your home to decrease your bushfire risk. By managing the vegetation around your home, you can create space by reducing fuels and therefore bushfire intensity. This helps to protect your house from direct flame contact and reduces the radiant heat to which your house will be exposed.</p>
								<ul>
